(function () {
  'use strict';

  function loadScript(src) {
    return new Promise(function (resolve, reject) {
      var s = document.createElement('script');
      s.src = src;
      s.onload = resolve;
      s.onerror = reject;
      document.head.appendChild(s);
    });
  }

  function downloadMaintenancePlan(options) {
    var waterType = options.waterType || 'Pool';
    var volumeGallons = options.volumeGallons || '—';
    var recommendations = options.recommendations || '';
    var schedule = options.schedule || [];
    var usageLevel = (options.usageLevel || 'medium').replace(/^./, function (c) { return c.toUpperCase(); });

    if (window.jspdf && window.jspdf.jsPDF) {
      generateWithJsPDF(waterType, volumeGallons, recommendations, schedule, usageLevel);
      return;
    }
    loadScript('https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js')
      .then(function () {
        generateWithJsPDF(waterType, volumeGallons, recommendations, schedule, usageLevel);
      })
      .catch(function () {
        alert('Could not load PDF library. Try again or print this page.');
        window.print();
      });
  }

  function stripHtml(html) {
    var div = document.createElement('div');
    div.innerHTML = html;
    return (div.textContent || div.innerText || '').replace(/\s+/g, ' ').trim();
  }

  function generateWithJsPDF(waterType, volumeGallons, recommendations, schedule, usageLevel) {
    var doc = new window.jspdf.jsPDF({ orientation: 'portrait', unit: 'pt', format: 'a4' });
    var margin = 40;
    var y = 40;
    var lineHeight = 16;
    var pageHeight = doc.internal.pageSize.height;

    function addText(text, opts) {
      opts = opts || {};
      var font = opts.bold ? 'helvetica' : 'helvetica';
      var size = opts.size || 11;
      doc.setFontSize(size);
      doc.setFont(font, opts.bold ? 'bold' : 'normal');
      var lines = doc.splitTextToSize(String(text), doc.internal.pageSize.width - margin * 2);
      var i;
      for (i = 0; i < lines.length; i++) {
        if (y > pageHeight - 50) {
          doc.addPage();
          y = margin;
        }
        doc.text(lines[i], margin, y);
        y += lineHeight;
      }
      doc.setFont('helvetica', 'normal');
      doc.setFontSize(11);
      return y;
    }

    addText('Water Balance — Maintenance Plan', { size: 18, bold: true });
    y += 10;
    addText('Water type: ' + waterType);
    addText('Volume: ' + volumeGallons + ' gallons');
    addText('Usage level: ' + usageLevel);
    y += 10;
    addText('Chemical recommendations', { bold: true });
    y += 4;
    addText(stripHtml(recommendations));
    y += 10;
    addText('Maintenance schedule', { bold: true });
    y += 4;
    schedule.forEach(function (item) {
      addText('• ' + item);
    });
    y += 10;
    addText('Generated by WaterBalanceTools.com — For reference only. Follow product labels.');
    doc.save('maintenance-plan.pdf');
  }

  window.WaterBalance = window.WaterBalance || {};
  window.WaterBalance.pdfGenerator = {
    downloadMaintenancePlan: downloadMaintenancePlan
  };
})();
